// Basic Commands

// clone - cloning a repository in our local machine
// git clone <-some link->

// status- display the state of code
// git status

// add - adds new or changed files in your working directory to the git staging area.
// git add <-file name->
// git add .
// modified files status changes to staged after add command

// commit - it is the record of change/
// git commit -m "some message"
// staged files changes to unmodied files after commit
// git commit -am "some message" (add and commit at same time, file must be added atlest once)

// push - upload local repo content to remote repo
// git push origin main
// origin - location where your repo is
// main - branch

// git push -u origin main (u means upstream)
// then you only have write git push
// git will automatically push to origin main only



